second_grad <- function(x){
  t <- matrix(0,3,3)
  t[1,1] <- 2 + 2*x[2]^2*x[3]^2
  t[1,2] <- 4*x[1]*x[2]*x[3]^2
  t[1,3] <- 4*x[1]*x[2]^2*x[3]
  t[2,1] <- 4*x[1]*x[2]*x[3]^2
  t[2,2] <- 2/3 + 2*x[1]^2*x[3]^2
  t[2,3] <- 4*x[1]^2*x[2]*x[3]
  t[3,1] <- 4*x[1]*x[2]^2*x[3]
  t[3,2] <- 4*x[1]^2*x[2]*x[3]
  t[3,3] <- 1/2 + 2*x[1]^2*x[2]^2
  return (t)}
x <- c(3,2,2)
alpha <- 0.2
for(iter in 1:80){
  x <- x - alpha*grad(x)%*%solve(second_grad(x))
}
print(x)
